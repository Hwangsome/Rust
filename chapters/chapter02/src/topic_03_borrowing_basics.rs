// 这一节只讨论“借用规则本身”，先不把函数参数和返回值混进来。
// 运行后重点观察三件事：
// 1. 为什么可以同时有多个不可变借用。
// 2. 为什么可变借用必须独占。
// 3. 为什么不是“永远不能先读后改”，而是“读和改的使用区间不能重叠”。
pub fn run() {
    println!("== Borrowing Basics ==");

    let mut values = vec![4, 5, 6];

    // 第一阶段：先只做“只读借用”。
    //
    // `&values` 表示“借来看一看”，但不拿走所有权，也不允许修改。
    // 因为下面两个借用都只是读取，所以它们可以同时存在。
    //
    // 你可以把它理解成：
    // - 多个人同时看同一本书，没有问题
    // - 因为大家都只是看，没有人会改书里的内容
    {
        let ref1 = &values;
        let ref2 = &values;
        println!("immutable refs => {:?}, {:?}", ref1, ref2);

        // 下面这段代码如果取消注释，会编译报错：
        //
        // let ref3 = &mut values;
        //
        // 常见错误信息会类似：
        // error[E0502]: cannot borrow `values` as mutable because it is also borrowed as immutable
        //
        // 这里要理解“为什么不行”：
        // - `ref1` 和 `ref2` 还在使用同一份数据
        // - 如果此时又允许 `ref3` 去修改这份数据
        // - 就会变成“一边有人在读，一边有人在改”
        //
        // Rust 不允许这种“读写同时发生”的情况，
        // 因为这会让程序状态变得难以推断。
        //
        // 所以规则不是“故意刁难你”，
        // 而是强制保证：
        // 多个读者可以一起存在，
        // 但写者出现时，必须保证自己是唯一的那个访问者。
    }

    // 注意上面那对大括号。
    // 它的作用是把 ref1 和 ref2 的“使用区间”明确限制在那个小块里。
    // 一旦离开这个作用域，Rust 就知道：
    // “前面的不可变借用已经结束了。”
    //
    // 这也是初学者最容易误解的地方：
    // 不是说“只要曾经有过不可变借用，以后就永远不能可变借用”，
    // 而是说“它们不能在同一时刻同时有效”。

    // 第二阶段：前面的只读借用结束后，才创建一个可变借用。
    //
    // `&mut values` 表示“我要独占地借来改一改”。
    // 这里的“独占”非常重要。
    //
    // 你可以把它理解成：
    // 现在不是“多人一起看书”，
    // 而是“一个人拿着笔正在改书里的内容”。
    // 这种时候 Rust 要求：不要再让其他人同时读或改。
    {
        let ref3 = &mut values;
        ref3.push(7);
        println!("mutable ref after immutable refs end => {:?}", ref3);

        // 如果这里再试图创建第二个可变借用，也会报错：
        //
        // let ref4 = &mut values;
        //
        // 原因同样是：可变借用要求独占访问权。
        // Rust 不希望两个地方同时都能修改同一份数据。
    }

    // 第三阶段：可变借用结束后，又可以重新创建不可变借用。
    // 这再次说明 Rust 看的不是“先后顺序”本身，
    // 而是“借用的有效区间有没有重叠”。
    let ref4 = &values;
    println!("immutable ref after mutable ref ends => {:?}", ref4);

    println!("规则总结：同一时刻可以有多个不可变借用，或者一个可变借用，但不能同时混用。");
    println!();
}
