// 这一节讲两件事：
// 1. 结合性：连续出现同一类运算符时，Rust 默认怎么分组。
// 2. 运算符重载：为什么我们自己定义的类型也能写 `+`。
//
// 这一节面向初学者，所以先只抓最核心的观察点：
// 1. `20 - 5 - 3` 和 `20 - (5 - 3)` 的结果不同。
// 2. `p1 + p2` 能成立，是因为我们手动告诉了 Rust：
//    “两个 Point 相加时，应该怎么加。”
use std::ops::Add;

// 这是一个最简单的自定义类型。
// 你可以把它理解成“平面上的一个点”，里面只有两个坐标：x 和 y。
#[derive(Clone, Copy, Debug)]
struct Point {
    x: i32,
    y: i32,
}

// `impl Add for Point` 可以先用一句人话理解：
// “给 Point 这个类型实现加法能力。”
//
// 这里的 `Add` 来自标准库。
// 你现在可以先把它粗略理解成：
// “谁实现了 Add，谁就可以定义自己的 `+` 应该怎么工作。”
//
// 所以这整段代码的目的不是写语法技巧，
// 而是在告诉 Rust：
// “如果以后看到 `Point + Point`，就按这里的规则来算。”
impl Add for Point {
    // `type Output = ...` 的意思是：
    // 这个加法做完以后，结果是什么类型。
    //
    // `Self` 表示“当前这个类型自己”。
    // 当前这个类型就是 `Point`，
    // 所以这一行其实等价于：
    //
    // type Output = Point;
    //
    // 也就是说：
    // `Point + Point` 的结果，仍然是一个 `Point`。
    type Output = Self;

    // `fn add(...)` 就是 `+` 背后真正会调用的函数。
    //
    // 这里的参数也要看懂：
    // - `self`：`+` 左边的值
    // - `rhs`：`+` 右边的值
    //
    // `rhs` 是 right-hand side 的缩写，
    // 也就是“右边那个操作数”的意思。
    //
    // 所以如果你写：
    //
    // let sum = p1 + p2;
    //
    // 你可以先把它近似理解成：
    //
    // let sum = p1.add(p2);
    //
    // 这里只是帮助理解，先不用纠结更底层的细节。
    fn add(self, rhs: Self) -> Self::Output {
        // 这里才是“Point 的加法规则”本体。
        //
        // 我们规定：
        // - 新点的 x = 左边点的 x + 右边点的 x
        // - 新点的 y = 左边点的 y + 右边点的 y
        //
        // 如果：
        // p1 = Point { x: 1, y: 2 }
        // p2 = Point { x: 3, y: 4 }
        //
        // 那结果就会是：
        // Point { x: 4, y: 6 }
        Self {
            x: self.x + rhs.x,
            y: self.y + rhs.y,
        }
    }
}

pub fn run() {
    // 第一部分：看“结合性”。
    //
    // 这里先不要把“结合性”和“优先级”混在一起。
    //
    // 这一行里只有减号：
    // 20 - 5 - 3
    //
    // 它们的优先级相同，所以这里讨论的不是“谁先算”，
    // 而是“默认怎么分组”。
    //
    // 对减法来说，这里按左边开始分组：
    // (20 - 5) - 3
    //
    // 所以结果是：
    // 15 - 3 = 12
    let without_parentheses = 20 - 5 - 3;

    // 这一行因为加了括号，所以分组方式变了：
    // 20 - (5 - 3)
    //
    // Rust 会先算括号里的：
    // 5 - 3 = 2
    //
    // 然后再算：
    // 20 - 2 = 18
    let with_parentheses = 20 - (5 - 3);

    // 这里要观察的是：
    // 明明都是减法，只因为分组不同，结果就不同。
    println!("20 - 5 - 3 = {without_parentheses}");
    println!("20 - (5 - 3) = {with_parentheses}");

    // 第二部分：看“运算符重载”。
    //
    // 对数字来说，`+` 本来就是语言内建支持的。
    // 但对 `Point` 这种我们自己定义的类型，默认其实不能直接写 `+`。
    //
    // 之所以现在能写，是因为上面已经写了：
    // `impl Add for Point`
    //
    // 也就是：我们已经把“Point 的加法规则”告诉 Rust 了。
    let p1 = Point { x: 1, y: 2 };
    let p2 = Point { x: 3, y: 4 };

    // 这一行的意思不是“编译器自动猜到怎么加结构体”，
    // 而是“按我们刚才定义好的 add 规则来加”。
    let sum = p1 + p2;

    // 所以这里应该看到：
    // x = 1 + 3 = 4
    // y = 2 + 4 = 6
    println!("operator overloading with Add trait => {:?}", sum);
    println!();
}
